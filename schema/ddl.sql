CREATE TABLE IF NOT EXISTS sync_task_checkpoint (
    id BIGINT AUTO_INCREMENT PRIMARY KEY COMMENT '主键',
    task_name VARCHAR(64) NOT NULL COMMENT '任务名',
    chain_id BIGINT NOT NULL COMMENT '链ID',
    contract_address VARCHAR(42) NOT NULL COMMENT '合约地址',
    start_block BIGINT UNSIGNED NOT NULL DEFAULT 0 COMMENT '起始区块',
    last_scanned_block BIGINT UNSIGNED NOT NULL DEFAULT 0 COMMENT '最新已入库区块',
    last_safe_block BIGINT UNSIGNED NOT NULL DEFAULT 0 COMMENT '本轮安全区块=最新区块-延迟块数',
    delay_blocks INT UNSIGNED NOT NULL DEFAULT 6 COMMENT '延迟确认块数',
    status VARCHAR(16) NOT NULL DEFAULT 'INIT' COMMENT 'INIT/RUNNING/IDLE',
    updated_at TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP,
    created_at TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,
    UNIQUE KEY uk_task_chain_contract (task_name, chain_id, contract_address)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COMMENT='扫链断点表';

CREATE TABLE IF NOT EXISTS akapledge_event_log (
    id BIGINT AUTO_INCREMENT PRIMARY KEY COMMENT '主键',
    chain_id BIGINT NOT NULL COMMENT '链ID',
    contract_address VARCHAR(42) NOT NULL COMMENT '合约地址',
    block_number BIGINT UNSIGNED NOT NULL COMMENT '区块号',
    block_hash VARCHAR(66) NOT NULL COMMENT '区块hash',
    tx_hash VARCHAR(66) NOT NULL COMMENT '交易hash',
    tx_index INT UNSIGNED NOT NULL COMMENT '交易索引',
    log_index INT UNSIGNED NOT NULL COMMENT '日志索引',
    event_name VARCHAR(64) NOT NULL COMMENT '事件名',
    pool_id VARCHAR(78) NOT NULL DEFAULT '' COMMENT '池ID',
    user_address VARCHAR(42) NOT NULL DEFAULT '' COMMENT '用户地址',
    topic0 VARCHAR(66) NOT NULL COMMENT '事件签名',
    payload_json JSON NOT NULL COMMENT '完整事件json',
    removed TINYINT(1) NOT NULL DEFAULT 0 COMMENT '是否被重组移除',
    created_at TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,
    UNIQUE KEY uk_tx_log (tx_hash, log_index),
    KEY idx_contract_block (chain_id, contract_address, block_number),
    KEY idx_pool_event (pool_id, event_name),
    KEY idx_user_event (user_address, event_name)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COMMENT='原始事件流水表';

CREATE TABLE IF NOT EXISTS akapledge_pool (
