    contract_address VARCHAR(42) NOT NULL COMMENT '合约地址',
    pool_id VARCHAR(78) NOT NULL COMMENT '池ID',
    lender_address VARCHAR(42) NOT NULL COMMENT '出借人地址',
    lend_deposited VARCHAR(78) NOT NULL DEFAULT '0' COMMENT '累计出借',
    lend_refunded VARCHAR(78) NOT NULL DEFAULT '0' COMMENT '累计退款',
    matched_principal_share VARCHAR(78) NOT NULL DEFAULT '0' COMMENT '撮合本金份额',
    sp_minted VARCHAR(78) NOT NULL DEFAULT '0' COMMENT '铸造SP数量',
    sp_burned VARCHAR(78) NOT NULL DEFAULT '0' COMMENT '销毁SP数量',
    stable_withdrawn VARCHAR(78) NOT NULL DEFAULT '0' COMMENT '提取稳定币',
    last_event_block BIGINT UNSIGNED NOT NULL DEFAULT 0 COMMENT '最后事件区块',
    updated_at TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP,
