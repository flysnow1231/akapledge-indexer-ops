    collateral_ratio_bps VARCHAR(78) NOT NULL DEFAULT '0' COMMENT '抵押率bps',
    liquidation_threshold_bps VARCHAR(78) NOT NULL DEFAULT '0' COMMENT '清算阈值bps',
    protocol_fee_bps VARCHAR(78) NOT NULL DEFAULT '0' COMMENT '协议费bps',
    slippage_bps VARCHAR(78) NOT NULL DEFAULT '0' COMMENT '滑点bps',
    total_lend_deposited VARCHAR(78) NOT NULL DEFAULT '0' COMMENT '总出借金额',
    total_collateral_deposited VARCHAR(78) NOT NULL DEFAULT '0' COMMENT '总抵押金额',
    matched_principal VARCHAR(78) NOT NULL DEFAULT '0' COMMENT '撮合本金',
    used_collateral_amount VARCHAR(78) NOT NULL DEFAULT '0' COMMENT '占用抵押物',
    unmatched_lend_total VARCHAR(78) NOT NULL DEFAULT '0' COMMENT '未撮合出借',
    unmatched_collateral_total VARCHAR(78) NOT NULL DEFAULT '0' COMMENT '未撮合抵押',
    stable_loan_paid_total VARCHAR(78) NOT NULL DEFAULT '0' COMMENT '已放款总额',
    stable_lend_refund_paid_total VARCHAR(78) NOT NULL DEFAULT '0' COMMENT '已退还出借总额',
    collateral_borrow_refund_paid_total VARCHAR(78) NOT NULL DEFAULT '0' COMMENT '已退还抵押总额',
    latest_collateral_value_stable VARCHAR(78) NOT NULL DEFAULT '0' COMMENT '最近一次健康检查的抵押价值',
    latest_debt_total VARCHAR(78) NOT NULL DEFAULT '0' COMMENT '最近一次健康检查的债务总额',
    latest_ratio_bps VARCHAR(78) NOT NULL DEFAULT '0' COMMENT '最近一次健康检查抵押率',
    latest_health_result TINYINT(1) NOT NULL DEFAULT 0 COMMENT '最近一次健康检查是否安全',
    stable_to_lenders_total VARCHAR(78) NOT NULL DEFAULT '0' COMMENT '结算后分给出借人的稳定币总额',
    stable_to_borrowers_surplus_total VARCHAR(78) NOT NULL DEFAULT '0' COMMENT '结算后分给借款人的稳定币盈余',
    collateral_to_borrowers_total VARCHAR(78) NOT NULL DEFAULT '0' COMMENT '结算后返给借款人的抵押物总量',
    finish_collateral_sold VARCHAR(78) NOT NULL DEFAULT '0' COMMENT 'finish/liquidation卖出的抵押物',
    finish_stable_balance_before VARCHAR(78) NOT NULL DEFAULT '0' COMMENT 'finish/liquidation前稳定币余额',
    finish_stable_balance_after VARCHAR(78) NOT NULL DEFAULT '0' COMMENT 'finish/liquidation后稳定币余额',
    finish_block_number BIGINT UNSIGNED NOT NULL DEFAULT 0 COMMENT 'finish/liquidation所在区块',
    last_event_block BIGINT UNSIGNED NOT NULL DEFAULT 0 COMMENT '最后事件区块',
    updated_at TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP,
    created_at TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,
    UNIQUE KEY uk_chain_contract_pool (chain_id, contract_address, pool_id),
    KEY idx_stage_settle (stage, settle_time),
    KEY idx_last_event_block (last_event_block)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COMMENT='运营池汇总表';

CREATE TABLE IF NOT EXISTS akapledge_lender_position (
    id BIGINT AUTO_INCREMENT PRIMARY KEY COMMENT '主键',
